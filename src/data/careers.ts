// Helper function to get career by ID
export const getCareerById = (id: string): CareerRecommendation | undefined => {
  return careers.find(career => career.id === id);
};

// Helper function to calculate career match based on assessment scores
export const calculateCareerMatches = (assessmentScores: {
  aptitude: number;
  interests: number;
  personality: number;
  emotionalIntelligence: number;
  skillsReadiness: number;
}): CareerRecommendation[] => {
  // Simple matching algorithm - can be enhanced with ML
  const matchedCareers = careers.map(career => {
    // Calculate match percentage based on various factors
    let matchScore = 0;
    
    // Weight different assessment categories
    matchScore += assessmentScores.aptitude * 0.2;
    matchScore += assessmentScores.interests * 0.3;
    matchScore += assessmentScores.personality * 0.2;
    matchScore += assessmentScores.emotionalIntelligence * 0.15;
    matchScore += assessmentScores.skillsReadiness * 0.15;
    
    // Add some randomization for variety
    const randomFactor = Math.random() * 10;
    matchScore = Math.min(100, matchScore + randomFactor);
    
    return {
      ...career,
      matchPercentage: Math.round(matchScore)
    };
  });
  
  // Sort by match percentage and return top matches
  return matchedCareers.sort((a, b) => (b.matchPercentage || 0) - (a.matchPercentage || 0));
};

// Get top N career recommendations
export const getTopCareerRecommendations = (
  assessmentScores: {
    aptitude: number;
    interests: number;
    personality: number;
    emotionalIntelligence: number;
    skillsReadiness: number;
  },
  limit: number = 3
): CareerRecommendation[] => {
  const matches = calculateCareerMatches(assessmentScores);
  return matches.slice(0, limit);
};